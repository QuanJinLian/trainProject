package dto;

public class zipCodeBin {
	private String zipcode, sido, gugun, dong, bldg, bunji, seq;

	public String getZipcode() {
		return zipcode;
	}

	public zipCodeBin setZipcode(String zipcode) {
		this.zipcode = zipcode;
		return this;
	}

	public String getSido() {
		return sido;
	}

	public zipCodeBin setSido(String sido) {
		this.sido = sido;
		return this;
	}

	public String getGugun() {
		return gugun;
	}

	public zipCodeBin setGugun(String gugun) {
		this.gugun = gugun;
		return this;
	}

	public String getDong() {
		return dong;
	}

	public zipCodeBin setDong(String dong) {
		this.dong = dong;
		return this;
	}

	public String getBldg() {
		return bldg;
	}

	public zipCodeBin setBldg(String bldg) {
		this.bldg = bldg;
		return this;
	}

	public String getBunji() {
		return bunji;
	}

	public zipCodeBin setBunji(String bunji) {
		this.bunji = bunji;
		return this;
	}

	public String getSeq() {
		return seq;
	}

	public zipCodeBin setSeq(String seq) {
		this.seq = seq;
		return this;
	}

}
